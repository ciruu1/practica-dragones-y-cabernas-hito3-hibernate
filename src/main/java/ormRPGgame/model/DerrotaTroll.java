package ormRPGgame.model;

public class DerrotaTroll {
    private int id_t;
    private int codigo_t;

    public DerrotaTroll(int id_t, int codigo_t) {
        this.id_t = id_t;
        this.codigo_t = codigo_t;
    }

    public int getId_t() {
        return id_t;
    }

    public int getCodigo_t() {
        return codigo_t;
    }
}
